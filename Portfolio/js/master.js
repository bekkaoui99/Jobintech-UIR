multiLanguage.addEventListener("click", (e) => {
  if (
    e.target.classList.contains("btn-language") &&
    !e.target.classList.contains("active")
  ) {
    multiLanguage.querySelector(".active").classList.remove("active");
    e.target.classList.add("active");
  }
});

function switchLanguage(language) {
  var language = MultiLanguageContents[language];

  // navBat content
  home.textContent = language.navbar.home;
  about.textContent = language.navbar.about;
  resume.textContent = language.navbar.resume;
  project.textContent = language.navbar.project;
  contact.textContent = language.navbar.contact;

  //landing page contenct:
  greeding.textContent = language.landingPage.greeting;
  devStack.textContent = language.landingPage.devStack;
  downloadCV.textContent = language.landingPage.buttonCV;
  moreAboutMe.textContent = language.landingPage.buttonAboutMe;

  // about content
  aboutMeCardContent.innerText = language.aboutMe.information;

  // project content
  blogDescriptionContent.innerText = language.project.Blog.descriptionContent;
  ecommerceDescriptionContent.innerText =
    language.project.Ecommerce.descriptionContent;
  eventDescriptionContent.innerText =
    language.project.EventManagement.descriptionContent;
  cardDescriptionContent.innerText =
    language.project.QuestionCardGame.descriptionContent;

  // education content
  educationMasterDate.textContent = language.education.masterDegree.date;
  educationMasterPlace.textContent = language.education.masterDegree.place;
  educationMasterTitle.textContent = language.education.masterDegree.title;

  educationBachelorDate.textContent = language.education.Bachelor.date;
  educationBachelorPlace.textContent = language.education.Bachelor.place;
  educationBachelorTitle.textContent = language.education.Bachelor.title;

  educationAssociateDegreeDate.textContent =
    language.education.AssociateDegree.date;
  educationAssociateDegreePlace.textContent =
    language.education.AssociateDegree.place;
  educationAssociateDegreeTitle.textContent =
    language.education.AssociateDegree.title;

  // experience content
  experienceCNRSTDate.textContent = language.experience.CNRST.date;
  experienceCNRSTPlace.textContent = language.experience.CNRST.place;
  experienceCNRSTTitle.textContent = language.experience.CNRST.experienceTitle;
  experienceCNRSTDescription.textContent =
    language.experience.CNRST.experienceDescriptions;

  experienceUIRDate.textContent = language.experience.UIR.date;
  experienceUIRPlace.textContent = language.experience.UIR.place;
  experienceUIRTitle.textContent = language.experience.UIR.experienceTitle;
  experienceUIRDescription.textContent =
    language.experience.UIR.experienceDescriptions;
}

